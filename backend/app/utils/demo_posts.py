def build_demo_posts(start_index: int = 0, count: int = 12) -> list[dict[str, str | None]]:
    posts: list[dict[str, str]] = []

    for offset in range(count):
        index = start_index + offset
        topic, subtitle = TOPIC_PAIRS[index % len(TOPIC_PAIRS)]
        hook = HOOK_PATTERNS[index % len(HOOK_PATTERNS)]
        value = VALUE_PATTERNS[(index + 1) % len(VALUE_PATTERNS)]
        cta = CTA_PATTERNS[(index + 2) % len(CTA_PATTERNS)]

        title = f"{topic}: {subtitle}"
        text = (
            f"{hook}\n\n"
            f"Суть видео: {value}\n\n"
            f"Почему это работает: автор быстро показывает проблему, дает короткое объяснение и закрепляет мысль через повторяемый визуальный элемент.\n\n"
            f"Монтаж и динамика: кадры меняются достаточно часто, чтобы не проседало внимание, но не настолько быстро, чтобы зритель терял нить повествования.\n\n"
            f"CTA и упаковка: {cta}\n\n"
            f"Практический вывод: такой формат хорошо подходит для экспертных и полуэкспертных ниш, где важно одновременно обучать, удерживать и подводить к следующему действию."
        )
        posts.append(
            {
                "title": title,
                "text": text,
                "video_url": None,
                "poster_url": None,
                "source_url": None,
            }
        )

    return posts
